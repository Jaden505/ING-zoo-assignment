package com.ing.zoo;

public class Crocodile {
    public String name;

    public Crocodile() {
    }

    public void sayHello() {
        System.out.println("Crocodile " + name + " says hello!");
    }

    public void eatMeat() {
        System.out.println("Crocodile " + name + " is eating meat.");
    }

    public void doTrick() {
        System.out.println("Crocodile " + name + " snaps its jaws loudly!");
    }
}