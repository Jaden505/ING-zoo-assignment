package com.ing.zoo;

public class Giraffe {
    public String name;

    public Giraffe() {
    }

    public void sayHello() {
        System.out.println("Giraffe " + name + " says hello!");
    }

    public void eatLeaves() {
        System.out.println("Giraffe " + name + " is eating leaves.");
    }

    public void doTrick() {
        System.out.println("Giraffe " + name + " gracefully stands on its hind legs!");
    }
}
